- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions
{
    self.window = [[UIWindow alloc] initWithFrame:[[UIScreen mainScreen] bounds]];
    self.localidades_model = [[Localidades alloc] init];
    self.dias_model =  [[DiasFestivos alloc] init];
    
    // Override point for customization after application launch.
    settings = [[Settings alloc] init];

    if ( ![settings isSetted]) {
        self.viewController = [[SettingsViewController alloc] initWithNibName:@"SettingsViewController" bundle:nil]  ; 
    }else {
        [self showCalendar];
    }
    self.window.rootViewController = self.viewController;
    [self.window makeKeyAndVisible];
    return YES;
}

-(void) showCalendar
{
    if ([[UIDevice currentDevice] userInterfaceIdiom] == UIUserInterfaceIdiomPhone) {
        self.viewController = [[ViewController alloc] initWithNibName:@"ViewController_iPhone" bundle:nil];
    } else {
        self.viewController = [[ViewController alloc] initWithNibName:@"ViewController_iPad" bundle:nil];
    }
    self.window.rootViewController = self.viewController;

}
